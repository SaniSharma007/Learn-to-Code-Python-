# Dictionary: Collection of key and values
dct={'name':'Sanidhiya','age':21}
print(dct)
lst=[['name','Sanidhiya'],['surname','sharma']]
dict1=dict(lst)
print(dict1)
# len() function returns the length of the parameter passed
print(len(dict1))
# Delete a item in  the dictionary
del dict1['name']
print(dict1)
